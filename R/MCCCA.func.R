        }#else{
        # convergence<-FALSE
        #
      }

      #ite.ob<-ite.ob+1

      ###old####
      # para.list.now<-list(dummy.mat=dummy.mat,dummy.diag=dummy.diag,Qcate=B.para[[ite.cate]]
